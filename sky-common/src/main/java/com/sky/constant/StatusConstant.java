package com.sky.constant;

/**
 * 状态常量，用于表示启用或禁用状态
 * 主要用于业务逻辑中需要区分启用/禁用场景的统一常量管理
 */
public class StatusConstant {

    /**
     * 启用状态，值为1
     * 表示当前功能或数据处于启用状态
     */
    public static final Integer ENABLE = 1;

    /**
     * 禁用状态，值为0
     * 表示当前功能或数据处于禁用状态
     */
    public static final Integer DISABLE = 0;
}
